+ (char)toPinyinFirstLetter:(unsigned short)hanzi
{   
	int index = hanzi - HANZI_START;
	if (index >= 0 && index <= HANZI_COUNT)
		return toupper(firstLetterArray[index]);
    else if(isalpha(hanzi))
        return toupper(hanzi);
	else
		return '#';
}

+ (NSString *)toPinyinFirstStr:(NSString *) hanzi
{
    char letter = ' ';
    if(hanzi.length>0)
     letter = [Hanzi toPinyinFirstLetter:[hanzi characterAtIndex:0]];
    return [NSString stringWithFormat:@"%c",letter];
}

